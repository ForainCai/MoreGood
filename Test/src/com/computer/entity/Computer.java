package com.computer.entity;

public class Computer {
	String id;
	String brand;
	double price;
	Peizhi pz;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public Peizhi getPz() {
		return pz;
	}
	public void setPz(Peizhi pz) {
		this.pz = pz;
	}
	
}
